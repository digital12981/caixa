export const formatCurrency = (value: number): string => {
  return new Intl.NumberFormat('pt-BR', {
    style: 'currency',
    currency: 'BRL'
  }).format(value);
};

export const calculateDiscount = (price: number, evaluation: number): number => {
  return Math.round((1 - price / evaluation) * 100);
};
